function updateCartCount() {
  // 1. Calculate total quantity
  // Ensure 'cart' is defined or pulled from localStorage first!
  const currentCart = JSON.parse(localStorage.getItem('cart')) || [];
  const count = currentCart.reduce((acc, item) => acc + (item.qty || 1), 0);

  // 2. Target the existing ID from our HTML
  const badge = document.getElementById("cart-count");

  if (badge) {
    // 3. Update the text
    badge.textContent = count;

    // 4. Optional: Hide badge if cart is empty to look cleaner
    if (count === 0) {
      badge.style.display = "none";
    } else {
      badge.style.display = "block";
    }
  }
}

// Call it immediately so the number shows when the page loads
updateCartCount();

function attachAddCartListeners() {

  document.querySelectorAll(".add-to-cart").forEach(btn => {

    btn.addEventListener("click", e => {

      const id = e.target.getAttribute("data-id");

      const product = products.find(p => p.id == id);

      const existing = cart.find(p => p.id == id);

      if (existing) existing.qty += 1;

      else cart.push({ ...product, qty: 1 });

      localStorage.setItem("cart", JSON.stringify(cart));

      updateCartCount();

      e.target.textContent = "Added!";

      setTimeout(() => e.target.textContent = "Add to Cart", 1000);

    });

  });

}



